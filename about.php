            <h2 class="sub-heading">HISTORIQUE : </h2>
            <p class="post-text">La plate-forme KPH est née d’une idée partagée d’un groupe de cinq jeunes étudiants informaticiens dont quatre hommes et une femme,lauréat d’une compétition organisé par l’UNICEF et le PNUD appelé ''youth-Challenge''.KPH, Bâtir son parcours Professionnel</p>
                <br>
            <h2 class="sub-heading">ÉNONCÉE :</h2>
            <p class="post-text">KPH est une plateforme numérique d’orientation scolaire, universitaire et professionnelle; notre plateforme numérique est une solution optimale à portée de main pour l’orientation et l’accompagnement scolaire. Via Internet tout le monde a accès à notre plateforme sur toute l’étendue du territoire congolais et partout ailleurs. Elle informe, oriente et accompagne les jeunes grâce à une technologie spéciale et personnalisée.</p>
                <br>
            <h2 class="sub-heading">PROBLÉMATIQUE :</h2>
            <p class="post-text">- Le manque de structure et organisme d’orientation scolaire; universitaire et professionnel.</p>
            <p class="post-text">- Le retard causé par de nombreux échecs scolaires à cause du mauvais choix de parcours. et le mauvais choix du parcours.</p>
                <br>
            <h2 class="sub-heading">CONSÉQUENCES :</h2>
            <p class="post-text">- L’échec scolaire et académique.</p>
            <p class="post-text">- L’incompétence dans son domaine.</p>
            <p class="post-text">- Un niveau scolaire très bas malgré les années d’études.</p>
            <p class="post-text">- La réorientation professionnelle post académique.</p>
            <p class="post-text">- La délinquance juvénile.</p>
            <p class="post-text">- Le manque de cadres dans la société.</p>
                 <br>
            <h2 class="sub-heading">NOTRE MOTIVATION :</h2>
            <p class="post-text">La satisfaction de voir la montée d’une nouvelle génération de cadre compétent.
La création d’un espace et un environnement favorisant l’éclosion d’une jeunesse acteur de sa propre réussite et épandue.
Le développement intellectuel; sociale, culturelle et économique de la société
Le mauvais choix scolaire et expérience passée de ces cinq jeunes. Pour palier à ce problème, ils ont pensé mettre en place une plate-forme numérique d’orientation scolaire.</p>

            
